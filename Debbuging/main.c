// Функция для установки цвета RGB-светодиода (плавный переход по радуге)
void Set_RGB(uint16_t adc_val) {
    uint16_t r = 0, g = 0, b = 0;

    // Разбиваем диапазон АЦП (0-4095) на 6 цветовых секторов
    uint8_t sector = adc_val / 682;
    uint16_t offset = (adc_val % 682) * 999 / 682; // Приводим к диапазону ШИМ (0-999)

    if(sector == 0)      { r = 999; g = offset; b = 0; }
    else if(sector == 1) { r = 999 - offset; g = 999; b = 0; }
    else if(sector == 2) { r = 0; g = 999; b = offset; }
    else if(sector == 3) { r = 0; g = 999 - offset; b = 999; }
    else if(sector == 4) { r = offset; g = 0; b = 999; }
    else                 { r = 999; g = 0; b = 999 - offset; }

    // Отправляем скважность в каналы таймера 1 (в регистры сравнения)
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, r); // Красный
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, g); // Зеленый
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, b); // Синий
}

// Обработчик прерываний таймера 2 (здесь генерируется меандр для ЦАП)
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
    static uint8_t dac_state = 0;

    if (htim->Instance == TIM2) { // Если прерывание пришло от TIM2
        if (dac_state) {
            HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_12B_R, 4095); // 3.3 В
        } else {
            HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_12B_R, 0);    // 0 В
        }
        dac_state = !dac_state; // Меняем состояние на противоположное
    }
}
